# Operadores Aritméticos

# Adição
soma = 5 + 3
print("Soma:", soma)  # Resultado: 8

# Subtração
subtracao = 10 - 4
print("Subtracao:", subtracao)  # Resultado: 6

# Multiplicação
multiplicacao = 3 * 4
print("Multiplicacao:", multiplicacao)  # Resultado: 12

# Divisão
divisao = 20 / 5
print("Divisao:", divisao)  # Resultado: 4.0 (número de ponto flutuante)

# Divisão Inteira
divisao_inteira = 20 // 3
print("Divisao Inteira:", divisao_inteira)  # Resultado: 6 (inteiro, arredondado para baixo)

# Módulo (resto da divisão)
resto = 10 % 3
print("Resto:", resto)  # Resultado: 1

# Potência
potencia = 2 ** 3
print("Potencia:", potencia)  # Resultado: 8

# Operadores de Comparação

# Igual a
igual = 5 == 5
print("Igual a:", igual)  # Resultado: True

# Diferente de
diferente = 5 != 3
print("Diferente de:", diferente)  # Resultado: True

# Maior que
maior = 10 > 5
print("Maior que:", maior)  # Resultado: True

# Menor que
menor = 4 < 7
print("Menor que:", menor)  # Resultado: True

# Maior ou igual a
maior_igual = 5 >= 5
print("Maior ou igual a:", maior_igual)  # Resultado: True

# Menor ou igual a
menor_igual = 3 <= 2
print("Menor ou igual a:", menor_igual)  # Resultado: False
