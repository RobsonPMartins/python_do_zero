# Tipos de Dados em Python

# Inteiro (int)
idade = 30

# Número de ponto flutuante (float)
altura = 1.75

# String (str)
nome = "Alice"

# Lista (list)
frutas = ["maçã", "banana", "laranja"]

# Tupla (tuple)
coordenadas = (10, 20)

# Dicionário (dict)
informacoes = {"nome": "Alice", "idade": 30}

# Booleano (bool)
verdadeiro = True
falso = False

# Exibindo os valores e tipos de dados
print("Idade:", idade, "Tipo:", type(idade))
print("Altura:", altura, "Tipo:", type(altura))
print("Nome:", nome, "Tipo:", type(nome))
print("Frutas:", frutas, "Tipo:", type(frutas))
print("Coordenadas:", coordenadas, "Tipo:", type(coordenadas))
print("Informações:", informacoes, "Tipo:", type(informacoes))
print("Verdadeiro:", verdadeiro, "Tipo:", type(verdadeiro))
print("Falso:", falso, "Tipo:", type(falso))
