# Definindo variáveis
nome = "Alice"  # Variável para armazenar um nome
idade = 30      # Variável para armazenar uma idade
saldo = 1000.50  # Variável para armazenar um saldo

# Exibindo informações
print("Bem-vindo(a) ao nosso programa!")
print("Nome:", nome)     # Exibindo o nome
print("Idade:", idade)   # Exibindo a idade
print("Saldo:", saldo)   # Exibindo o saldo

# Realizando operações com as variáveis
idade += 5              # Adicionando 5 anos à idade
saldo -= 200.75         # Subtraindo 200.75 do saldo

# Exibindo informações atualizadas
print("\nApos algumas operacoes:")
print("Idade:", idade)   # Exibindo a idade atualizada
print("Saldo:", saldo)   # Exibindo o saldo atualizado
