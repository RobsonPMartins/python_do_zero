# Criando uma lista de números inteiros
numeros = [1, 2, 3, 4, 5]

# Imprimindo a lista
print(numeros)
# Usamos a função 'print' para mostrar o conteúdo da lista 'numeros' no console.
# Quando executamos este código, veremos a lista [1, 2, 3, 4, 5] impressa na tela.

# Iterando pela lista usando um loop 'for'
for numero in numeros:
    print(numero)  # Isso imprimirá cada número da lista
